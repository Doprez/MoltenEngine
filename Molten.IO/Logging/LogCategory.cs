namespace Molten;

public enum LogCategory
{
    /// <summary>
    /// A general message.
    /// </summary>
    Message = 0,

    /// <summary>
    /// A warning message.
    /// </summary>
    Warning = 1,

    /// <summary>
    /// An error message.
    /// </summary>
    Error = 2,

    /// <summary>
    /// A debug message.
    /// </summary>
    Debug = 3,
}
